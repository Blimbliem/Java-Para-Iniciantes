package chapterOne;

public class ExampleDois {
    public static void main (String[] args){
        int val1; // declaration of variable
        int val2; // declaration of variable

        val1 = 23; // initializatio of variable

        System.out.println("val1 contains: " + val1);

        val2 = val1 / 3; // initialization of variable

        System.out.println("val2 contains " + val1 + " / 3: " + val2);

        /*
            when we declare a variable, we use the parameters bellow:
            type name ;
            int val1; // declaration of variable
            when we initialize a variable, we use the parameters bellow:
            name = value ;
            val1 = 23; // initialization of variable
        */
    }

}
